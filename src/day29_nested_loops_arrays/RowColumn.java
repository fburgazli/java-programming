package day29_nested_loops_arrays;

public class RowColumn {
    public static void main(String[] args) {
        for (int i = 1; i <=3; i++) {
            for (int j = 1; j <=4; j++) {
                System.out.print(i +" | " +j +"\t");
            }
            System.out.println();

        }
    }
}
