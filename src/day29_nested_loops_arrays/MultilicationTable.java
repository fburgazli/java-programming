package day29_nested_loops_arrays;

public class MultilicationTable {
    public static void main(String[] args) {
        for (int i = 1; i <11 ; i++) {
            for (int j = 1; j < 11; j++) {

                System.out.print(i*j +"\t");

            }
            System.out.println();

        }
    }
}
