package day05_primitivies_concatenation;

public class BooleanPractice {
    public static void main(String[] args) {
        boolean isHungry = false;
        boolean isSleepy = false;
        boolean isJavaFun = true;
        boolean isLunchTime = true;

        System.out.println(isHungry); //false
        System.out.println(isSleepy); //false
        System.out.println(isJavaFun); //true
        System.out.println(isLunchTime); //true


    }
}
