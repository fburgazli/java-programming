package day25_for_loop;

public class ForLoopFirst {
    public static void main(String[] args) {
        /**infinite loop like while(true)
        for(;;){
            System.out.println("Java is fun!");
        }
        */
        //  (step1)first start with initial value, (step2)second check the condition and
        //  (step3)if true run the code and (step4) then update the value of i.
        for (int i = 1 ; i <= 10 ; i++) {
            System.out.print(i +" ");
        }


    }
}
