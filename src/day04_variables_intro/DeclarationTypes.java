package day04_variables_intro;

public class DeclarationTypes {
    public static void main(String[] args) {
        int apples;
        apples = 34;

        System.out.println(apples);

        int num = 100 ;
        System.out.println(num);

        num = 101;
        System.out.println(num);

        int studentsInNY , studentsInVA;
        studentsInNY = 52;
        studentsInVA = 205;
        System.out.println(studentsInNY);
        System.out.println(studentsInVA);

        int peopleInCity = 1000, birdsOnTree = 27, fishInLake = 800 ;
        System.out.println(peopleInCity);
        System.out.println(birdsOnTree);
        System.out.println(fishInLake);


    }
}
