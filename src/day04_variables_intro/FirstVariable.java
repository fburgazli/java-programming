package day04_variables_intro;

public class FirstVariable {
    public static void main(String[] args) {
        // declare a variable n that can store integer.
        int n;
        // assign a value to n
        n = 100;
        //print n
        System.out.println(n);
        int numberOfApples;
        numberOfApples = 15 ;
        System.out.println(numberOfApples);

        int numberOfBananas;
        numberOfBananas = 7 ;
        System.out.println(numberOfBananas);

    }
}
